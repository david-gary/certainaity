#!/usr/bin/env python3
"""ForenScope API latency benchmark.

Submits N synthetic JPEG images to the POST /v1/analyze endpoint and
measures per-request wall-clock latency.  Requires a running ForenScope
API server and a valid JWT bearer token.

Usage
-----
    python scripts/benchmark.py --url http://localhost:8000 \\
        --count 50 --token <jwt> [--image-size 256] [--concurrency 1]

The token can be generated by signing a claim set with the private key
produced by ``scripts/generate_keys.py``.
"""

from __future__ import annotations

import argparse
import io
import statistics
import sys
import time

from PIL import Image as PILImage


def _make_jpeg(size: int) -> bytes:
    buf = io.BytesIO()
    PILImage.new("RGB", (size, size), color=(128, 64, 32)).save(buf, format="JPEG")
    return buf.getvalue()


def _run_sequential(base_url: str, count: int, token: str, jpeg: bytes) -> list[float]:
    try:
        import httpx
    except ImportError:
        print("error: httpx is required — pip install httpx", file=sys.stderr)
        sys.exit(1)

    headers = {"Authorization": f"Bearer {token}"}
    latencies: list[float] = []
    errors = 0

    with httpx.Client(base_url=base_url, timeout=30.0) as client:
        for i in range(count):
            t0 = time.perf_counter()
            try:
                resp = client.post(
                    "/v1/analyze",
                    headers=headers,
                    files={"file": ("bench.jpg", io.BytesIO(jpeg), "image/jpeg")},
                )
                elapsed_ms = (time.perf_counter() - t0) * 1000
                if resp.status_code == 202:
                    latencies.append(elapsed_ms)
                    status = "ok"
                else:
                    errors += 1
                    status = f"HTTP {resp.status_code}"
            except Exception as exc:
                elapsed_ms = (time.perf_counter() - t0) * 1000
                errors += 1
                status = f"error: {exc}"

            print(f"  [{i + 1:>{len(str(count))}}/{count}]  {elapsed_ms:7.1f} ms  {status}")

    return latencies


def _print_summary(latencies: list[float], errors: int) -> None:
    if not latencies:
        print("\nNo successful requests — cannot compute statistics.", file=sys.stderr)
        sys.exit(1)

    s = sorted(latencies)
    p95_idx = max(0, int(len(s) * 0.95) - 1)
    p99_idx = max(0, int(len(s) * 0.99) - 1)

    print(f"\n{'─' * 40}")
    print(f"Requests:  {len(latencies)} ok / {errors} errors")
    print(f"Min:       {min(latencies):.1f} ms")
    print(f"Median:    {statistics.median(latencies):.1f} ms")
    print(f"P95:       {s[p95_idx]:.1f} ms")
    print(f"P99:       {s[p99_idx]:.1f} ms")
    print(f"Max:       {max(latencies):.1f} ms")
    print(f"Mean:      {statistics.mean(latencies):.1f} ms")
    if len(latencies) > 1:
        print(f"Std dev:   {statistics.stdev(latencies):.1f} ms")
    print(f"{'─' * 40}")


def main() -> None:
    parser = argparse.ArgumentParser(
        description="ForenScope API latency benchmark",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument(
        "--url",
        default="http://localhost:8000",
        help="API base URL",
    )
    parser.add_argument(
        "--count",
        type=int,
        default=20,
        help="Number of requests to send",
    )
    parser.add_argument(
        "--token",
        required=True,
        help="JWT Bearer token (RS256, signed with secrets/jwt_private.pem)",
    )
    parser.add_argument(
        "--image-size",
        type=int,
        default=256,
        metavar="PX",
        help="Edge length (px) of the synthetic square JPEG",
    )
    args = parser.parse_args()

    jpeg = _make_jpeg(args.image_size)
    print(
        f"Sending {args.count} requests to {args.url}/v1/analyze"
        f" ({args.image_size}×{args.image_size} JPEG, {len(jpeg):,} bytes each) …\n"
    )

    latencies = _run_sequential(args.url, args.count, args.token, jpeg)
    _print_summary(latencies, args.count - len(latencies))


if __name__ == "__main__":
    main()
